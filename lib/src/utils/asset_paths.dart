/// Contains constant paths to asset files used by the credit card package.
///
/// This class provides static constants for all bundled image assets,
/// including card type logos and the EMV chip image.
///
/// These paths are used internally by the package and should not need
/// to be referenced directly in application code.
class AssetPaths {
  const AssetPaths._();

  /// Path to the Visa card logo asset.
  static const String visa = 'icons/visa.png';

  /// Path to the Rupay card logo asset.
  static const String rupay = 'icons/rupay.png';

  /// Path to the Mastercard card logo asset.
  static const String mastercard = 'icons/mastercard.png';

  /// Path to the American Express card logo asset.
  static const String americanExpress = 'icons/amex.png';

  /// Path to the UnionPay card logo asset.
  static const String unionpay = 'icons/unionpay.png';

  /// Path to the Discover card logo asset.
  static const String discover = 'icons/discover.png';

  /// Path to the Elo card logo asset.
  static const String elo = 'icons/elo.png';

  /// Path to the Hipercard card logo asset.
  static const String hipercard = 'icons/hipercard.png';

  /// Path to the EMV chip image asset.
  static const String chip = 'icons/chip.png';

  /// Path to the Mir card logo asset.
  static const String mir = 'icons/mir.png';
}
